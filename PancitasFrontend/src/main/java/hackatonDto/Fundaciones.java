package hackatonDto;
public class Fundaciones {
	
    private int id;
    private String nombreFundacion;
    private String nombreRepresFundacion;
    private String emailFundacion;
    private String ciudadFundacion;
    private String direccionFundacion;
    private String telfFundacion;
    private String passFundacion;

	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getNombreFundacion() {
		return nombreFundacion;
	}
	public void setNombreFundacion(String nombreFundacion) {
		this.nombreFundacion = nombreFundacion;
	}
	public String getNombreRepresFundacion() {
		return nombreRepresFundacion;
	}
	public void setNombreRepresFundacion(String nombreRepresFundacion) {
		this.nombreRepresFundacion = nombreRepresFundacion;
	}
	public String getEmailFundacion() {
		return emailFundacion;
	}
	public void setEmailFundacion(String emailFundacion) {
		this.emailFundacion = emailFundacion;
	}
	public String getCiudadFundacion() {
		return ciudadFundacion;
	}
	public void setCiudadFundacion(String ciudadFundacion) {
		this.ciudadFundacion = ciudadFundacion;
	}
	public String getDireccionFundacion() {
		return direccionFundacion;
	}
	public void setDireccionFundacion(String direccionFundacion) {
		this.direccionFundacion = direccionFundacion;
	}
	public String getTelfFundacion() {
		return telfFundacion;
	}
	public void setTelfFundacion(String telfFundacion) {
		this.telfFundacion = telfFundacion;
	}
	public String getPassFundacion() {
		return passFundacion;
	}
	public void setPassFundacion(String passFundacion) {
		this.passFundacion = passFundacion;
	}
	    
	
}
