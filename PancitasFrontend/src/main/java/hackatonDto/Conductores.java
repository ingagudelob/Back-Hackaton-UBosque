package hackatonDto;
public class Conductores {

	private int id;
	private String nombreConductor ;
	private String placaCarroConductor;
	private String emailConductor;
	private String ciudadConductor;
	private String teflConductor;
	private String passConductor;	
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getNombreConductor() {
		return nombreConductor;
	}
	public void setNombreConductor(String nombreConductor) {
		this.nombreConductor = nombreConductor;
	}
	public String getPlacaCarroConductor() {
		return placaCarroConductor;
	}
	public void setPlacaCarroConductor(String placaCarroConductor) {
		this.placaCarroConductor = placaCarroConductor;
	}
	public String getEmailConductor() {
		return emailConductor;
	}
	public void setEmailConductor(String emailConductor) {
		this.emailConductor = emailConductor;
	}
	public String getCiudadConductor() {
		return ciudadConductor;
	}
	public void setCiudadConductor(String ciudadConductor) {
		this.ciudadConductor = ciudadConductor;
	}
	public String getTeflConductor() {
		return teflConductor;
	}
	public void setTeflConductor(String teflConductor) {
		this.teflConductor = teflConductor;
	}
	public String getPassConductor() {
		return passConductor;
	}
	public void setPassConductor(String passConductor) {
		this.passConductor = passConductor;
	}
	
}
