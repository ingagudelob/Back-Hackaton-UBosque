package hackatonDto;
public class Lideres {
	
	private int id;
	private String nombreLider;
	private String emailLider;
	private String ciudadLider;
	private String direccionLider;
	private String telfLider;
	private String passLider;
	

	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getNombreLider() {
		return nombreLider;
	}
	public void setNombreLider(String nombreLider) {
		this.nombreLider = nombreLider;
	}
	public String getEmailLider() {
		return emailLider;
	}
	public void setEmailLider(String emailLider) {
		this.emailLider = emailLider;
	}
	public String getCiudadLider() {
		return ciudadLider;
	}
	public void setCiudadLider(String ciudadLider) {
		this.ciudadLider = ciudadLider;
	}
	public String getDireccionLider() {
		return direccionLider;
	}
	public void setDireccionLider(String direccionLider) {
		this.direccionLider = direccionLider;
	}
	public String getTelfLider() {
		return telfLider;
	}
	public void setTelfLider(String telfLider) {
		this.telfLider = telfLider;
	}
	public String getPassLider() {
		return passLider;
	}
	public void setPassLider(String passLider) {
		this.passLider = passLider;
	}
	
	
	
}
